'use client';

import { useState } from 'react';

import { MonitorSmartphone, Layers, Rocket, Sparkles, Star, ArrowRight } from 'lucide-react';
import { Button } from '@/components/ui/button';
import { Container } from '@/components/ui/container';
import {
  Card,
  CardContent,
  CardDescription,
  CardFooter,
  CardHeader,
  CardTitle,
} from '@/components/ui/card';
import { SectionHeader } from '@/components/ui/section-header';
import { websitePackages as websitePackagesData } from '@/data/packages';
import type { PackageDetail } from '@/data/packages';
import { PackageModal } from '@/components/packages/package-modal';
import { MotionItem, MotionSection } from '@/components/ui/motion-section';
import { SaleBadge } from '@/components/sale';
import { DiscountedPrice } from '@/components/sale';

const iconMap = {
  monitorSmartphone: MonitorSmartphone,
  layers: Layers,
  rocket: Rocket,
  sparkles: Sparkles,
} as const;

export function WebsitePackagesSection() {
  const [selectedPackage, setSelectedPackage] = useState<PackageDetail | null>(null);
  const [modalOpen, setModalOpen] = useState(false);

  const handleOpen = (pkg: PackageDetail) => {
    setSelectedPackage(pkg);
    setModalOpen(true);
  };

  const handleOpenChange = (open: boolean) => {
    setModalOpen(open);
    if (!open) {
      setSelectedPackage(null);
    }
  };

  // Split packages: main 3 tiers + enterprise
  const mainPackages = websitePackagesData.filter((p) => p.id !== 'web-premium');
  const enterprisePkg = websitePackagesData.find((p) => p.id === 'web-premium');

  return (
    <section className="py-16 md:py-24">
      <Container className="space-y-10">
        <MotionSection className="space-y-10" as="div">
          <MotionItem>
            <SectionHeader
              align="center"
              eyebrow="Website Packages"
              title="Website Packages Snapshot"
              description="Choose a starting point built around performance, usability, and your growth goals."
              className="mx-auto max-w-3xl"
            />
          </MotionItem>

          {/* Main 3 packages in a row */}
          <MotionSection as="div" className="grid grid-cols-1 gap-6 md:grid-cols-3" delay={0.12}>
            {mainPackages.map((pkg, index) => {
              const Icon = iconMap[pkg.icon as keyof typeof iconMap] ?? MonitorSmartphone;
              const isMostPopular = pkg.id === 'web-starter';
              const isFeatured = pkg.id === 'web-starter';

              return (
                <MotionItem
                  key={pkg.id}
                  delay={index * 0.08}
                  triggerOnViewport={false}
                  className="h-full"
                >
                  <Card
                    id={pkg.id}
                    className={`bg-[var(--pv-bg)]/95 dark:bg-[var(--pv-surface)]/95 group relative flex h-full flex-col overflow-visible border transition-all duration-300 hover:-translate-y-1 ${
                      isFeatured
                        ? 'ring-[var(--pv-primary)]/20 border-[var(--pv-primary)] shadow-[0_26px_60px_-20px_rgba(63,0,233,0.35)] ring-2'
                        : 'border-[var(--pv-border)] hover:border-[var(--pv-primary)] hover:shadow-[0_26px_60px_-40px_rgba(63,0,233,0.75)]'
                    }`}
                  >
                    <SaleBadge packageId={pkg.id} />
                    {isMostPopular && (
                      <div className="absolute -top-3 left-1/2 z-10 -translate-x-1/2">
                        <span className="inline-flex items-center gap-1.5 rounded-full bg-[linear-gradient(135deg,var(--pv-primary),var(--pv-primary-2))] px-4 py-1.5 text-xs font-semibold uppercase tracking-wider text-white shadow-lg">
                          <Star className="h-3 w-3 fill-current" />
                          Most Popular
                        </span>
                      </div>
                    )}
                    <CardHeader className="flex items-start gap-4 border-b border-[var(--pv-border)] pb-6">
                      <span className="inline-flex h-12 w-12 shrink-0 items-center justify-center rounded-full bg-[linear-gradient(135deg,var(--pv-primary),var(--pv-primary-2))] text-white shadow-[0_22px_46px_-34px_rgba(63,0,233,0.85)] transition-transform duration-300 group-hover:-translate-y-1">
                        <Icon className="h-6 w-6" aria-hidden="true" />
                      </span>
                      <div className="space-y-3">
                        <p className="text-xs font-semibold uppercase tracking-[0.26em] text-[var(--pv-primary)]">
                          {pkg.level}
                        </p>
                        <CardTitle className="text-lg font-medium text-[var(--pv-text)]">
                          {pkg.name}
                        </CardTitle>
                        <div className="text-sm font-medium uppercase tracking-[0.22em] text-[var(--pv-text-muted)]">
                          <DiscountedPrice packageId={pkg.id} priceString={pkg.price} />
                        </div>
                        <CardDescription className="text-sm leading-6">
                          {pkg.summary}
                        </CardDescription>
                      </div>
                    </CardHeader>
                    <CardContent className="grid flex-1 gap-5 pt-6 text-sm text-[var(--pv-text-muted)] md:grid-rows-[auto,auto]">
                      <div className="min-h-[80px] space-y-2">
                        <p className="text-xs font-semibold uppercase tracking-[0.18em] text-[var(--pv-text)]">
                          Best for
                        </p>
                        <p className="text-sm leading-6 text-[var(--pv-text-muted)]">{pkg.ideal}</p>
                      </div>
                      <div className="min-h-[104px] space-y-2">
                        <p className="text-xs font-semibold uppercase tracking-[0.18em] text-[var(--pv-text)]">
                          Value
                        </p>
                        <p className="leading-6 text-[var(--pv-text-muted)]">{pkg.value}</p>
                      </div>
                    </CardContent>
                    <CardFooter className="mt-auto flex flex-col gap-3 border-t border-[var(--pv-border)] pt-6">
                      <div className="mb-3 space-y-2 text-xs uppercase tracking-[0.18em] text-[var(--pv-text-muted)]">
                        <div className="flex flex-wrap gap-3">
                          <span className="rounded-full border border-[var(--pv-border)] px-3 py-1">
                            Response: {pkg.support.response}
                          </span>
                          <span className="rounded-full border border-[var(--pv-border)] px-3 py-1">
                            Cadence: {pkg.support.cadence}
                          </span>
                        </div>
                      </div>
                      <Button
                        variant={isFeatured ? 'default' : 'secondary'}
                        size="sm"
                        className={`w-full ${isFeatured ? '' : 'dark:text-white'}`}
                        onClick={() => handleOpen(pkg)}
                      >
                        Learn More
                      </Button>
                    </CardFooter>
                  </Card>
                </MotionItem>
              );
            })}
          </MotionSection>

          {/* Enterprise as full-width CTA */}
          {enterprisePkg && (
            <MotionItem delay={0.36}>
              <Card className="border-[var(--pv-border)] bg-gradient-to-r from-[var(--pv-surface)] to-[var(--pv-bg)] p-8">
                <div className="flex flex-col items-center justify-between gap-6 md:flex-row">
                  <div className="flex items-center gap-6">
                    <span className="inline-flex h-14 w-14 shrink-0 items-center justify-center rounded-full bg-[linear-gradient(135deg,var(--pv-primary),var(--pv-primary-2))] text-white shadow-lg">
                      <Sparkles className="h-7 w-7" aria-hidden="true" />
                    </span>
                    <div className="space-y-1">
                      <p className="text-xs font-semibold uppercase tracking-[0.26em] text-[var(--pv-primary)]">
                        {enterprisePkg.level}
                      </p>
                      <h3 className="text-xl font-medium text-[var(--pv-text)]">
                        {enterprisePkg.name}
                      </h3>
                      <p className="text-sm text-[var(--pv-text-muted)]">{enterprisePkg.summary}</p>
                    </div>
                  </div>
                  <div className="flex flex-col items-center gap-3 text-center md:items-end md:text-right">
                    <p className="text-sm font-medium uppercase tracking-wider text-[var(--pv-text-muted)]">
                      Starting at $6,500 + retainer
                    </p>
                    <Button onClick={() => handleOpen(enterprisePkg)} className="gap-2">
                      Get Custom Proposal <ArrowRight className="h-4 w-4" />
                    </Button>
                  </div>
                </div>
              </Card>
            </MotionItem>
          )}
        </MotionSection>
      </Container>
      <PackageModal pkg={selectedPackage} open={modalOpen} onOpenChange={handleOpenChange} />
    </section>
  );
}
